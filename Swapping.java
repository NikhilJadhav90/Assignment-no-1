/*
Program to Swap Two Numbers
 */
public class Swapping {
    public static void main(String args[])
    {
       int x=4,y=5,t;
       
       t = x;  
       x = y;  
       y = t;  
       System.out.println("After swapping: X= "+x +"  Y= " + y);  
       System.out.println("");  
    }
}
